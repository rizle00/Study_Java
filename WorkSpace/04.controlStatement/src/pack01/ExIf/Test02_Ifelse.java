package pack01.ExIf;

public class Test02_Ifelse {
public static void main(String[] args) {
	int number=9;
	if(number%2==1) {
		System.out.println("홀수");
	} else {
		System.out.println("짝수");
	}
	if(number%3==0) {
		System.out.println("3의 배수");
	} else {
		System.out.println("3의 배수가 아닙니다.");
	}
	
}
}
